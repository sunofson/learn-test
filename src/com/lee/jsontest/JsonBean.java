package com.lee.jsontest;

public class JsonBean {
	private String name;

	
	public JsonBean() {
	}

	public JsonBean(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
